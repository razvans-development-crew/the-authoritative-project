export async function generate_random_string(length: number): Promise<string> {
  let result = "";

  for (let i = 0; i < length; i++) {
    result += CHARACTER_ARRAY[Math.floor(Math.random() * CHARACTER_ARRAY.length)];
  }

  return result;
}

export async function get_client_ip(request: Request, server: any): Promise<string> {
  const forwarded = request.headers.get("X-Forwarded-For");

  if (forwarded) {
    return forwarded.split(",")[0] ?? "unknown";
  }

  const real_ip = request.headers.get("X-Real-IP");

  if (real_ip) {
    return real_ip;
  }

  return server?.requestIP?.(request)?.address ?? "unknown";
}

export async function is_bot_ratelimited() {
  const response = await fetch("https://discord.com/api/v10/gateway", {
    method: "GET",
    headers: {
      "Authorization": await get_env_variable("DISCORD_TOKEN"),
    }
  });

  if (response.status == 429) {
    return {
      is_ratelimited: true,
      retry_after: response.headers.get("Retry-After"),
      reset_after: response.headers.get("X-RateLimit-Reset-After"),
      rate_limit_remaining: response.headers.get("X-RateLimit-Remaining"),
      rate_limit_scope: response.headers.get("X-RateLimit-Scope")
    };
  }

  return {
    is_ratelimited: false,
    retry_after: null,
    reset_after: null,
    rate_limit_remaining: null,
    rate_limit_scope: null
  };
}
